async function loadRollbackHistory(historyFilePath) {
    const [readError, content] = await safe(fs.readFile(historyFilePath, "utf-8"));
    if (readError) {
        if (readError.code === "ENOENT") {
            return [];
        }
        throw readError;
    }

    let parsed;
    try {
        parsed = JSON.parse(content);
    } catch (error) {
        throw new Error(`Invalid JSON in rollback history file: ${historyFilePath}`);
    }

    if (Array.isArray(parsed)) {
        return parsed;
    }
    if (parsed && Array.isArray(parsed.entries)) {
        return parsed.entries;
    }

    throw new Error(`Rollback history file must contain an array: ${historyFilePath}`);
}

async function saveRollbackHistory(historyFilePath, entries) {
    const directoryPath = path.dirname(historyFilePath);
    const [mkdirError] = await safe(fs.mkdir(directoryPath, { recursive: true }));
    if (mkdirError && directoryPath !== ".") {
        throw mkdirError;
    }

    const [writeError] = await safe(fs.writeFile(historyFilePath, `${JSON.stringify(entries, null, 2)}\n`, "utf-8"));
    if (writeError) {
        throw writeError;
    }
}

async function appendRollbackHistoryEntry(historyFilePath, entry) {
    const entries = await loadRollbackHistory(historyFilePath);
    entries.push(entry);
    await saveRollbackHistory(historyFilePath, entries);
}

function createRollbackHistoryEntryId() {
    return `rb_${Date.now()}_${Math.random().toString(36).slice(2, 10)}`;
}

function createRollbackRecapRow(entry, status) {
    const expenseId = entry.received_document_id || "-";
    return {
        file: truncateFilename(entry.file_name || "-"),
        date: entry.date || "-",
        description: entry.description || "ROLLBACK",
        extraeu: isExtraEuDescription(entry.description),
        amount_gross: entry.amount_gross ?? "-",
        tax_deductibility: entry.tax_deductibility ?? "-",
        entity_status: entry.entity_status || "-",
        entity_name: entry.entity_name || "-",
        entity_id: entry.entity_id || "-",
        status,
        id: expenseId,
        url: expenseId === "-" ? "-" : `${EXPENSE_DETAILS_URL}/${expenseId}`,
    };
}

function rollbackOptionLabel(entry) {
    const amount = entry.amount_gross ?? "-";
    const description = entry.description || "-";
    return `${entry.file_name || "-"} | id:${entry.received_document_id || "-"} | ${description} | ${amount}`;
}

function toErrorText(error) {
    if (!error) {
        return "unknown_error";
    }
    return error.response?.data?.error || error.message || String(error);
}

async function runRollbackMode({ historyFilePath, runLogPath, activeProvider }) {
    const history = await loadRollbackHistory(historyFilePath);

    const rollbackCandidates = history
        .map((entry, historyIndex) => ({
            entry,
            historyIndex,
        }))
        .filter(({ entry }) => {
            const status = entry?.status || "created";
            const canRollbackStatus = status === "created" || status === "rollback_partial";
            return canRollbackStatus && entry?.company_id && entry?.received_document_id;
        });

    if (rollbackCandidates.length === 0) {
        p.log.info(`No rollback candidates found in ${historyFilePath}`);
        return;
    }

    p.log.info(`Rollback history: ${historyFilePath}`);

    const selectedHistoryIndexes = await p.multiselect({
        message: "Select one or more expenses to rollback",
        options: rollbackCandidates.map(({ entry, historyIndex }) => ({
            value: historyIndex,
            label: rollbackOptionLabel(entry),
        })),
        required: true,
    });

    if (p.isCancel(selectedHistoryIndexes)) {
        p.log.error("Operation cancelled.");
        process.exit(0);
    }

    if (!selectedHistoryIndexes || selectedHistoryIndexes.length === 0) {
        p.log.warn("No expenses selected for rollback.");
        return;
    }

    const confirmed = await p.confirm({
        message: `Confirm rollback of ${selectedHistoryIndexes.length} expense(s)?`,
        active: "Yes, rollback",
        inactive: "No",
    });

    if (p.isCancel(confirmed) || !confirmed) {
        p.log.error("Operation cancelled.");
        process.exit(0);
    }

    const rollbackRecap = [];
    let successCount = 0;
    let partialCount = 0;
    let deleteSuccessCount = 0;
    let restoreSuccessCount = 0;

    for (const historyIndex of selectedHistoryIndexes) {
        const entry = history[historyIndex];
        if (!entry) {
            continue;
        }

        const errors = [];
        let remoteDeleted = false;
        let fileRestored = false;

        const [deleteError] = await safe(deleteReceivedDocument(entry.company_id, entry.received_document_id));
        if (deleteError) {
            if (deleteError.response?.status === 404) {
                // Already deleted remotely in a previous attempt.
                remoteDeleted = true;
                deleteSuccessCount += 1;
            } else {
                errors.push(`delete: ${toErrorText(deleteError)}`);
            }
        } else {
            remoteDeleted = true;
            deleteSuccessCount += 1;
        }

        const sourcePath = entry.source_path ? path.resolve(entry.source_path) : null;
        const donePath = entry.done_path ? path.resolve(entry.done_path) : null;

        if (!sourcePath || !donePath) {
            errors.push("restore: missing source_path or done_path in history");
        } else {
            const [doneAccessError] = await safe(fs.access(donePath));
            if (doneAccessError) {
                errors.push(`restore: done file not accessible (${donePath})`);
            } else {
                const [sourceAccessError] = await safe(fs.access(sourcePath));
                if (!sourceAccessError) {
                    errors.push(`restore: source file already exists (${sourcePath})`);
                } else if (sourceAccessError.code !== "ENOENT") {
                    errors.push(`restore: source file not accessible (${sourcePath})`);
                } else {
                    const [mkdirError] = await safe(fs.mkdir(path.dirname(sourcePath), { recursive: true }));
                    if (mkdirError) {
                        errors.push(`restore: unable to create source dir (${toErrorText(mkdirError)})`);
                    } else {
                        const [restoreError] = await safe(fs.rename(donePath, sourcePath));
                        if (restoreError) {
                            errors.push(`restore: ${toErrorText(restoreError)}`);
                        } else {
                            fileRestored = true;
                            restoreSuccessCount += 1;
                        }
                    }
                }
            }
        }

        const nowIso = new Date().toISOString();
        const isSuccess = remoteDeleted && fileRestored;
        const newStatus = isSuccess ? "rolled_back" : "rollback_partial";
        const rollbackEvent = {
            at: nowIso,
            action: "rollback",
            remote_deleted: remoteDeleted,
            file_restored: fileRestored,
            errors,
        };
        const previousEvents = Array.isArray(entry.rollback_events) ? entry.rollback_events : [];

        history[historyIndex] = {
            ...entry,
            status: newStatus,
            updated_at: nowIso,
            rollback_events: [...previousEvents, rollbackEvent],
        };

        if (isSuccess) {
            successCount += 1;
            p.log.success(`Rolled back ${entry.file_name} [expense id: ${entry.received_document_id}]`);
        } else {
            partialCount += 1;
            p.log.warn(
                `Rollback partial for ${entry.file_name} [expense id: ${entry.received_document_id}] - ${errors.join(
                    "; "
                )}`
            );
        }

        rollbackRecap.push(createRollbackRecapRow(history[historyIndex], isSuccess ? "rolled-back" : "rollback-partial"));
    }

    await saveRollbackHistory(historyFilePath, history);

    console.table(rollbackRecap);
    p.log.info(
        `Rollback complete: success ${successCount}, partial ${partialCount}, remote deleted ${deleteSuccessCount}, files restored ${restoreSuccessCount}`
    );

    const [logError] = await safe(
        appendRunLog({
            executionType: "rollback",
            activeProvider,
            filesFound: selectedHistoryIndexes.length,
            recap: rollbackRecap,
            llmRecap: createEmptyLlmRecap(),
            logFilePath: runLogPath,
        })
    );

    if (logError) {
        p.log.warn(`Unable to append run log at ${runLogPath}: ${logError.message || String(logError)}`);
    } else {
        p.log.info(`Run log updated: ${runLogPath}`);
    }
}

function toRoundedNumber(value, decimals = 8) {
    if (!Number.isFinite(value)) {
        return null;
    }

    return Number(value.toFixed(decimals));
}

function computeCost(tokens, unitPrice) {
    if (!Number.isFinite(tokens) || !Number.isFinite(unitPrice)) {
        return null;
    }

    return tokens * unitPrice;
}

function createEmptyLlmTotals() {
    return {
        input_tokens: 0,
        output_tokens: 0,
        total_tokens: 0,
        input_cost_usd: 0,
        output_cost_usd: 0,
        total_cost_usd: 0,
    };
}

function mergeLlmTotals(target, source) {
    if (Number.isFinite(source.input_tokens)) {
        target.input_tokens += source.input_tokens;
    }
    if (Number.isFinite(source.output_tokens)) {
        target.output_tokens += source.output_tokens;
    }
    if (Number.isFinite(source.total_tokens)) {
        target.total_tokens += source.total_tokens;
    }
    if (Number.isFinite(source.input_cost_usd)) {
        target.input_cost_usd += source.input_cost_usd;
    }
    if (Number.isFinite(source.output_cost_usd)) {
        target.output_cost_usd += source.output_cost_usd;
    }
    if (Number.isFinite(source.total_cost_usd)) {
        target.total_cost_usd += source.total_cost_usd;
    }
}

async function buildLlmCostRecap(llmUsageRows) {
    const rowsWithPriceModel = llmUsageRows.map((row) => ({
        ...row,
        llm_price_model_id: toLlmPriceModelId({
            provider: row.provider,
            model: row.model,
        }),
    }));

    const uniquePriceModelIds = [...new Set(rowsWithPriceModel.map((row) => row.llm_price_model_id).filter(Boolean))];

    let pricesByModelId = {};
    let missingModelIds = [];
    let pricingFetchError = null;

    if (uniquePriceModelIds.length > 0) {
        const [pricingError, pricingResult] = await safe(fetchModelPricesById(uniquePriceModelIds));
        if (pricingError) {
            pricingFetchError = pricingError.message || String(pricingError);
        } else {
            pricesByModelId = pricingResult.pricesByModelId;
            missingModelIds = pricingResult.missingModelIds;
        }
    }

    const totals = createEmptyLlmTotals();
    const totalsByProviderModel = {};

    const rowsWithCosts = rowsWithPriceModel.map((row) => {
        const price = row.llm_price_model_id ? pricesByModelId[row.llm_price_model_id] : null;

        const inputCost = computeCost(row.input_tokens, price?.prompt);
        const outputCost = computeCost(row.output_tokens, price?.completion);
        const totalCost = Number.isFinite(inputCost) && Number.isFinite(outputCost) ? inputCost + outputCost : null;

        const normalizedRow = {
            ...row,
            input_cost_usd: inputCost,
            output_cost_usd: outputCost,
            total_cost_usd: totalCost,
        };

        mergeLlmTotals(totals, normalizedRow);

        const providerModelKey = `${row.provider || "unknown"}|${row.model || "unknown"}`;
        if (!totalsByProviderModel[providerModelKey]) {
            totalsByProviderModel[providerModelKey] = {
                provider: row.provider || "unknown",
                model: row.model || "unknown",
                llm_price_model_id: row.llm_price_model_id || null,
                ...createEmptyLlmTotals(),
            };
        }
        mergeLlmTotals(totalsByProviderModel[providerModelKey], normalizedRow);

        return {
            file: row.file,
            provider: row.provider || "unknown",
            model: row.model || "unknown",
            llm_price_model_id: row.llm_price_model_id || "n/a",
            input_tokens: row.input_tokens,
            output_tokens: row.output_tokens,
            total_tokens: row.total_tokens,
            input_cost_usd: toRoundedNumber(inputCost),
            output_cost_usd: toRoundedNumber(outputCost),
            total_cost_usd: toRoundedNumber(totalCost),
        };
    });

    return {
        pricingFetchError,
        missingModelIds,
        rowsWithCosts,
        totals: {
            input_tokens: totals.input_tokens,
            output_tokens: totals.output_tokens,
            total_tokens: totals.total_tokens,
            input_cost_usd: toRoundedNumber(totals.input_cost_usd),
            output_cost_usd: toRoundedNumber(totals.output_cost_usd),
            total_cost_usd: toRoundedNumber(totals.total_cost_usd),
        },
        totalsByProviderModel: Object.values(totalsByProviderModel).map((row) => ({
            ...row,
            input_cost_usd: toRoundedNumber(row.input_cost_usd),
            output_cost_usd: toRoundedNumber(row.output_cost_usd),
            total_cost_usd: toRoundedNumber(row.total_cost_usd),
        })),
    };
}

/**
 * Processes a PDF file with the configured LLM provider and normalizes invoice data
 * @param {Buffer} pdfBuffer - The PDF file buffer to process
 * @returns {Promise<Object>} The extracted and processed invoice data
 * @throws {Error} If processing fails
 */
async function processPDFWithLLM(pdfBuffer) {
    // Read the prompt from the file
    const [promptError, prompt] = await safe(fs.readFile("prompt.txt", "utf-8"));
    if (promptError) {
        console.error("Error reading prompt file:", promptError);
        throw promptError;
    }

    const [extractError, extractionResult] = await safe(
        extractInvoiceFromPdf({
            pdfBuffer,
            prompt,
        })
    );

    if (extractError) {
        console.error("Error processing PDF with LLM provider:", extractError);
        throw extractError;
    }

    const parsed = extractionResult?.data;
    if (!parsed || typeof parsed !== "object") {
        throw new Error("LLM provider did not return structured extraction data");
    }

    // Parse and format the date
    if (parsed.date) {
        try {
            // Parse the date in DD.MM.YYYY format
            const parsedDate = parse(parsed.date, "dd.MM.yyyy", new Date());
            // Format it in ISO format for consistency
            parsed.date = format(parsedDate, "yyyy-MM-dd");
        } catch (dateError) {
            console.error("Error parsing date from the document.");
            throw dateError;
        }
    }

    // Ensure total_amount is a number
    if (parsed.total_amount) {
        parsed.total_amount =
            typeof parsed.total_amount === "string"
                ? parseFloat(parsed.total_amount.replace("€", "").trim())
                : parsed.total_amount;
    }

    // Process itemized expenses
    if (Array.isArray(parsed.itemized_expenses)) {
        parsed.itemized_expenses = parsed.itemized_expenses.map((item) => ({
            ...item,
            amount: typeof item.amount === "string" ? parseFloat(item.amount.replace("€", "").trim()) : item.amount,
            period_start: item.period
                ? parse(item.period.split(" - ")[0], "dd.MM.yyyy", new Date()).toISOString()
                : null,
            period_end: item.period ? parse(item.period.split(" - ")[1], "dd.MM.yyyy", new Date()).toISOString() : null,
        }));
    }

    parsed.description = normalizeDescriptionForExpense(parsed);
    if (!parsed.description || typeof parsed.description !== "string") {
        parsed.description = "SPESA";
    }

    // console.log("parsed", parsed);

    return {
        data: parsed,
        usage: extractionResult?.usage || null,
    };
}

function detectCountryFromAddress(address, vatNumber) {
    // First try from VAT number as it's more reliable
    if (vatNumber) {
        const vatPrefix = vatNumber.substring(0, 2).toUpperCase();
        const vatCountries = {
            DE: "Germania",
            IT: "Italia",
            FR: "Francia",
            GB: "Regno Unito",
            ES: "Spagna",
            NL: "Paesi Bassi",
            BE: "Belgio",
            PT: "Portogallo",
            AT: "Austria",
            CH: "Svizzera",
        };
        if (vatCountries[vatPrefix]) {
            return vatCountries[vatPrefix];
        }
    }

    // If no country from VAT, try from address
    if (address) {
        const addressUpper = address.toUpperCase();
        const countryIndicators = {
            GERMANY: "Germania",
            DEUTSCHLAND: "Germania",
            MÜNCHEN: "Germania",
            MUNCHEN: "Germania",
            FRANCE: "Francia",
            ITALIA: "Italia",
            ITALY: "Italia",
            SPAIN: "Spagna",
            ESPAÑA: "Spagna",
            UK: "Regno Unito",
            "UNITED KINGDOM": "Regno Unito",
            NETHERLANDS: "Paesi Bassi",
            NEDERLAND: "Paesi Bassi",
            BELGIUM: "Belgio",
            BELGIQUE: "Belgio",
            PORTUGAL: "Portogallo",
            AUSTRIA: "Austria",
            SWITZERLAND: "Svizzera",
            SCHWEIZ: "Svizzera",
        };

        for (const [indicator, country] of Object.entries(countryIndicators)) {
            if (addressUpper.includes(indicator)) {
                return country;
            }
        }
    }

    return null;
}

/**
 * Formats the data for creating a received document
 * @param {Object} params - Parameters for formatting the document data
 * @param {Object} params.extractedData - Data extracted from the PDF
 * @param {Object} params.supplier - Supplier information
 * @param {Object} params.paymentAccount - Payment account details
 * @param {Object} params.currency - Currency information
 * @returns {Object} Formatted document data
 */
function formatReceivedDocumentData({ extractedData, supplier, paymentAccount, currency }) {
    const amountGross = extractedData.total_amount;
    const amountVat = extractedData.vat_amount || 0;

    const paidDate = getPaymentDateForExpense(extractedData);

    return {
        type: "expense",
        entity: {
            id: supplier.id,
        },
        date: extractedData.date,
        description: extractedData.description,
        amount_net: amountGross - amountVat,
        amount_vat: amountVat,
        amount_gross: amountGross,
        tax_deductibility: getTaxDeductibilityForExpense(extractedData),
        items_list:
            extractedData.itemized_expenses?.map((item) => ({
                description: item.description || "",
                amount: item.amount,
                period_start_date: item.period_start || null,
                period_end_date: item.period_end || null,
            })) || [],
        payments_list: [
            {
                amount: amountGross,
                due_date: extractedData.date,
                paid_date: paidDate,
                status: "paid",
                payment_terms: {
                    days: 0,
                    type: "standard",
                },
                payment_account: {
                    id: paymentAccount.id,
                },
            },
        ],
        currency,
        language: {
            code: "it",
            name: "Italiano",
        },
    };
}

