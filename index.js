function truncateFilename(file) {
    return file.length > 30 ? file.substring(0, 30) : file;
}

function isExtraEuDescription(description) {
    return typeof description === "string" && description.toLowerCase().includes("extraeu");
}

function toDisplayValue(value) {
    if (value === null || value === undefined || value === "") {
        return "-";
    }
    if (typeof value === "number" && Number.isFinite(value)) {
        return Number(value.toFixed(8)).toString();
    }

    return String(value);
}

function escapeMarkdownCell(value) {
    return toDisplayValue(value).replaceAll("|", "\\|").replaceAll("\n", " ");
}

function createMarkdownTable(columns, rows) {
    const header = `| ${columns.join(" | ")} |`;
    const separator = `| ${columns.map(() => "---").join(" | ")} |`;
    const body = rows.map((row) => `| ${columns.map((column) => escapeMarkdownCell(row[column])).join(" | ")} |`);

    return [header, separator, ...body].join("\n");
}

async function ensureRunLogFile(logFilePath) {
    const [accessError] = await safe(fs.access(logFilePath));
    if (!accessError) {
        return;
    }

    const directoryPath = path.dirname(logFilePath);
    const [mkdirError] = await safe(fs.mkdir(directoryPath, { recursive: true }));
    if (mkdirError && directoryPath !== ".") {
        throw mkdirError;
    }

    const [writeError] = await safe(
        fs.writeFile(
            logFilePath,
            "# Run Log\n\nThis file is auto-generated by the importer. Each run appends a new section.\n\n"
        )
    );
    if (writeError) {
        throw writeError;
    }
}

async function appendRunLog({ executionType, activeProvider, filesFound, recap, llmRecap, logFilePath }) {
    await ensureRunLogFile(logFilePath);

    const nowIso = new Date().toISOString();
    const lines = [
        `## Run ${nowIso}`,
        `- Mode: \`${executionType}\``,
        `- Provider: \`${activeProvider}\``,
        `- Files found: ${filesFound}`,
        `- Expenses processed: ${recap.length}`,
        "",
        "### Expenses",
    ];

    if (recap.length > 0) {
        lines.push(
            createMarkdownTable(
                [
                    "file",
                    "date",
                    "description",
                    "extraeu",
                    "amount_gross",
                    "tax_deductibility",
                    "entity_status",
                    "entity_name",
                    "entity_id",
                    "status",
                    "id",
                    "url",
                ],
                recap
            )
        );
    } else {
        lines.push("_No expense rows._");
    }

    lines.push("");
    lines.push("### LLM Totals");
    lines.push(
        createMarkdownTable(
            ["input_tokens", "output_tokens", "total_tokens", "input_cost_usd", "output_cost_usd", "total_cost_usd"],
            [llmRecap.totals]
        )
    );
    lines.push("");
    lines.push("### LLM Usage Per File");
    lines.push(
        createMarkdownTable(
            [
                "file",
                "provider",
                "model",
                "llm_price_model_id",
                "input_tokens",
                "output_tokens",
                "total_tokens",
                "total_cost_usd",
            ],
            llmRecap.rowsWithCosts
        )
    );

    if (llmRecap.totalsByProviderModel.length > 1) {
        lines.push("");
        lines.push("### LLM Totals By Provider/Model");
        lines.push(
            createMarkdownTable(
                [
                    "provider",
                    "model",
                    "llm_price_model_id",
                    "input_tokens",
                    "output_tokens",
                    "total_tokens",
                    "total_cost_usd",
                ],
                llmRecap.totalsByProviderModel
            )
        );
    }

    if (llmRecap.pricingFetchError) {
        lines.push("");
        lines.push(`- Pricing fetch warning: ${llmRecap.pricingFetchError}`);
    }

    if (llmRecap.missingModelIds.length > 0) {
        lines.push("");
        lines.push(`- Missing price model ids: ${llmRecap.missingModelIds.join(", ")}`);
    }

    lines.push("");
    lines.push("---");
    lines.push("");

    const [appendError] = await safe(fs.appendFile(logFilePath, lines.join("\n")));
    if (appendError) {
        throw appendError;
    }
}

